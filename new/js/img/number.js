function CurentTime(now) {
	if(now==null||now==undefined||now==''){
		 now = new Date();
	}
	

	var clock = [];
	var hh = now.getHours(); 
	var mm = now.getMinutes(); 

	if(hh<10){
		clock[0] = 0;
		clock[1] = hh;
	}
	else{
		clock[0] = Math.floor(hh/10);
		clock[1] = hh%10;
	}
	if(mm<10){
		clock[2] = 0;
		clock[3] = mm;
	}
	else{
		clock[2] = Math.floor(mm/10);
		clock[3] = mm%10;
	}
	return clock;
}
